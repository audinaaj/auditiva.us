
    <h3>Minimum Requirements</h3>
    <p>Before downloading our fitting software, verify your computer meets the minimum operating system requirements.</p>
    <br/>
    <h4>Hardware</h4>
    <ul>
        <li>Intel Core i3 2GHz or AMD A8 processor, or greater.</li>
        <li>2 GB of RAM.</li>
        <li>Serial port or USB port (when using a USB port, you also need a USB-Serial Adapter to connect to a Hi-Pro programming box).</li>
        <li>2 GB of free hard disk space.</li>
        <li>1024 x 768 display resolution.</li>
        <li>Programming Box: HI-PRO, NOAHlink, EMiniTec.</li>
    </ul>
    <br/>
    <h4>Software</h4>
    <ul>
        <li>Microsoft Windows XP(SP3)/Vista/7/8/10.</li>
        <li>Microsoft .NET Framework 4.0.3 or higher.</li>
        <li>Drivers for USB-Serial Adapter (if using a USB-Serial Adapter to connect to a Hi-Pro Classic programming box).</li>
        <li>Noah 4.1 or higher (optional).</li>
    </ul>

